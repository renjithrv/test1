class Play_game_slot_details(models.Model):
   created_at = models.DateTimeField(auto_now_add=True)
   updated_at = models.DateTimeField(auto_now=True)
   play_fixture_id = models.IntegerField()
   play_game_day = models.CharField(max_length=255)
   play_venue_id = models.IntegerField()
   play_game_slot = models.CharField(max_length=100)
   play_court_name = models.CharField(max_length=30)
   play_game_max_players = models.IntegerField()
   play_home_batch_takein = models.IntegerField()
   play_home_batch_start_time = models.CharField(max_length=10)
   play_flexi_batch_start_time = models.CharField(max_length=10)
   play_general_cut_off = models.CharField(max_length=10)
   play_game_version = models.CharField(max_length=5)
   play_game_rating = models.CharField(max_length=15)
   play_enable_fixture = models.CharField(max_length=4)
   play_fixture_reminder_sent = models.CharField(max_length=255)
   play_flexi_fixture_reminder_sent = models.CharField(max_length=255)
   play_assigned_buddy1 = models.IntegerField()
   play_assigned_buddy2 = models.IntegerField()
   play_temp_buddy1 = models.IntegerField()
   play_temp_buddy2 = models.IntegerField()
   play_session_description = models.CharField(max_length=255)

